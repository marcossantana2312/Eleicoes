package br.ucsal.eleicoes.model;

public class Consulta {
	
	private String consulta;
	private Long qtd;
	
	public String getConsulta() {
		return consulta;
	}
	public void setConsulta(String consulta) {
		this.consulta = consulta;
	}
	public Long getQtd() {
		return qtd;
	}
	public void setQtd(Long qtd) {
		this.qtd = qtd;
	}

}
